# Creating a tuple
my_tuple = (1, 2, 3, 4)
print(my_tuple)

# Accessing items
print(my_tuple[0])
print(my_tuple[-1])

#Traversing a tuple
for item in my_tuple:
    print(item)

#NOTE THAT: TUPLES ARE IMMUTABLE YOU CAN NOT CHANGE THE VALUES
#For example: my_tuple[0] = 32 is gonna throw an error